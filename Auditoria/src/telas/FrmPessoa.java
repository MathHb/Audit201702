     public void preencherTabela(){
          
          connex.conexao();
    
        ArrayList dados = new ArrayList();
        
        String [] Colunas = new String[]{"COD_PESSOA", "NOME", "TELEFONE","DOCUMENTO","USUARIO_ID"};
    
        connex.executaSql("select * from pessoa where usuario_id ='"+ codUsuarioPronto +"'");
        try {
            connex.rs.first();
             do{
                    dados.add(new Object[]{connex.rs.getInt("COD_PESSOA"), connex.rs.getString("NOME"), connex.rs.getString("TELEFONE"),
                                           connex.rs.getString("DOCUMENTO"), connex.rs.getInt("USUARIO_ID") });
                 
               }while(connex.rs.next()); 
        } catch (SQLException ex) {
             //JOptionPane.showMessageDialog(null, "Erro!!\n ERRO:"+ ex);
        }
       
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);  
        jTableEstado.setModel(modelo);
        jTableEstado.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTableEstado.getColumnModel().getColumn(0).setResizable(false);
        jTableEstado.getColumnModel().getColumn(1).setPreferredWidth(100);
        jTableEstado.getColumnModel().getColumn(1).setResizable(false);
        jTableEstado.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTableEstado.getColumnModel().getColumn(2).setResizable(false);
        jTableEstado.getColumnModel().getColumn(3).setPreferredWidth(100);
        jTableEstado.getColumnModel().getColumn(3).setResizable(false);
        jTableEstado.getColumnModel().getColumn(4).setPreferredWidth(100);
        jTableEstado.getColumnModel().getColumn(4).setResizable(false);
        jTableEstado.getTableHeader().setReorderingAllowed(false);
        jTableEstado.setAutoResizeMode(jTableEstado.AUTO_RESIZE_OFF);
    
        jTableEstado.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    } 
