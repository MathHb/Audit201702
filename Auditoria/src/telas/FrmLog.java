      public void preencherTabela(){
          
          conecta.conexao();
    
        ArrayList dados = new ArrayList();
        
        String [] Colunas = new String[]{"ID_PESSOA", "ID_USUARIO", "Operacao","Ano-mes-dia","Hora"};
    
        conecta.executaSql("select * from pessoa_audit where usuario ='"+ String.valueOf(FrmLogin.codUsuarioPronto) +"'");
        try {
            conecta.rs.first();
             do{
                    dados.add(new Object[]{conecta.rs.getInt("COD_PESSOA_AUDIT"), conecta.rs.getString("USUARIO"), conecta.rs.getString("OPERACAO"),
                                           conecta.rs.getDate("DATA"), conecta.rs.getTime("HORA") });
                 
               }while(conecta.rs.next()); 
        } catch (SQLException ex) {
            // JOptionPane.showMessageDialog(null, "Erro!!\n ERRO:"+ ex);
        }
       
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);  
        jTableEstado.setModel(modelo);
        jTableEstado.getColumnModel().getColumn(0).setPreferredWidth(80);
        jTableEstado.getColumnModel().getColumn(0).setResizable(false);
        jTableEstado.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTableEstado.getColumnModel().getColumn(1).setResizable(false);
        jTableEstado.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTableEstado.getColumnModel().getColumn(2).setResizable(false);
        jTableEstado.getColumnModel().getColumn(3).setPreferredWidth(100);
        jTableEstado.getColumnModel().getColumn(3).setResizable(false);
        jTableEstado.getColumnModel().getColumn(4).setPreferredWidth(60);
        jTableEstado.getColumnModel().getColumn(4).setResizable(false);
        jTableEstado.getTableHeader().setReorderingAllowed(false);
        jTableEstado.setAutoResizeMode(jTableEstado.AUTO_RESIZE_OFF);
    
        jTableEstado.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    } 
    
